def get_ahc_type(msg_uni):
    """
    Return the possible form of Anomalous Hall Coefficient matrix for a given
    magnetic space group.

    Parameters
    ----------
    msg_unit : int
        Magnetic space group number (1..1651).

    Returns
    -------
    ahc_type : int
        Type of AHC matrix as defined in ahc_types.
        Zero means no Anomalous Hall effect is possible.

    Raises
    ------
    ValueError
        if msg_uni is not in [1..1651] range
    """

    if msg_uni < 0 or msg_uni > 1651:
        raise ValueError(
            "Magnetic space group id should be in [1..1651] range.!")

    mpg_litvin = uni_2_litvin[msg_uni-1]
    ahc_was_found = False
    for i in range(1, len(ahc_types)):
        for mpg in ahc_mpgs[i-1]:
            if mpg_litvin.find(mpg) != -1:
                ahc_was_found = True
                ahc_type = i
                break
        else:
            continue
        break

    if not ahc_was_found:
        ahc_type = 0

    return ahc_type
